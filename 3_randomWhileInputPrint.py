# Импортируем модуль random для генерации случайных чисел
# описание файла: Программа создает массив где количество указывает пользователь, заполняет его случайными числами от 1 до 100 и выводит на экран.

import random

# Запрашиваем у пользователя количество элементов массива
while True:
    try:
        kolElemMas = int(input("Введите количество элементов массива чисел: "))
        if kolElemMas < 0:
            print("Количество элементов не может быть отрицательным. Попробуйте снова.")
            continue
        break
    except ValueError:
        print("Пожалуйста, введите целое число.")

# Создаем массив с случайными числами от 1 до 100
randMassiv = [random.randint(1, 100) for _ in range(kolElemMas)]

# Выводим полученный массив
if randMassiv:
    print(f"\nВаш массив со случайными числами от 1 до 100:\n\n{randMassiv}\n")
else:
    print("\nМассив пуст.")

